// Fetch =
// sebuah method pada API javascript untuk mengambil resources pada jaringan,
// dan mengembalikan promise yang selesai (fullfilled) ketika ada response yang tersedia.

// fetch( resources, init );
// resources :
// 1. URL = alamat dari sumber yang kita ambil.
// 2. Request Object = representasi dari permintaan sumber.

// init (optional) = konfigurasi tambahan pada sebuah request berbentuk object.
// method, headers, body, mode, cache, referrer, referrerPolicy, integrity, keepalive, signal.

// response = hasil dari fetch berupa promise
// response (property) = headers, ok, redirected, status, statusText, type, url, body.
// response (method) : clone(), error(), redirect(), blob(), formData(), json(), text()
